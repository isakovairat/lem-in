#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t index;

	index = ft_strlen(s1);
	while (n-- && (*(s1 + index) = *s2++))
		index++;
	*(s1 + index) = 0;
	return (s1);
}
